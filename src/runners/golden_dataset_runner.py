import asyncio
import os
import sys
import time
from loguru import logger

# Add project root to sys.path so we can run this script directly
project_root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
if project_root not in sys.path:
    sys.path.insert(0, project_root)

from src.configs.settings import settings
from src.configs.db import init_db_pool, close_db_pool

# Dependency Injection Components
from src.llm.llm_factory import ILLMFactory
from src.llm.gemini_factory import GeminiLLMFactory
from src.dao.golden_record_dao import PgVectorGoldenRecordDAO
from src.evaluator.dataset_generator import LangchainDatasetGenerator

class GoldenDatasetRunner:
    """
    Orchestrator for the Synthetic Ground Truth Generation Pipeline.
    Manages high-concurrency LLM calls and transactional persistence.
    """
    
    def __init__(self, dao: PgVectorGoldenRecordDAO, generator: LangchainDatasetGenerator):
        self.dao = dao
        self.generator = generator

    async def run(self, batch_name: str, sample_size: int, topics: list = None) -> None:
        logger.info(f"Starting Golden Dataset Synthesis | Batch: '{batch_name}' | Target Size: {sample_size}")
        logger.info(f"Domain Restrictions (Topics): {topics if topics else 'None (Full Corpus)'}")
        
        start_time = time.time()
        
        # 1. Extract Seed Data from Vector DB
        logger.info("Phase 1: Fetching Seed Contexts...")
        chunks = await self.dao.get_random_seed_chunks(limit=sample_size, topics=topics)
        
        if not chunks:
            logger.error("Failed to retrieve any seed chunks from the database. Is the corpus ingested?")
            return
            
        logger.info(f"Successfully retrieved {len(chunks)} candidate chunks. Beginning generation phase.")
        
        # 2. Highly Concurrent LLM Synthesis
        # We fire off all N generation tasks simultaneously.
        # The generator handles Pydantic validation internally and returns None on failure/junk chunks.
        logger.info("Phase 2: Concurrent LLM Synthesis (Teacher Model)...")
        tasks = [
            self.generator.agenerate_qa_from_chunk(chunk=c, batch_name=batch_name) 
            for c in chunks
        ]
        
        # asyncio.gather executes them in parallel.
        raw_results = await asyncio.gather(*tasks)
        
        # Filter out None values (where the LLM rejected the chunk as useless)
        valid_records = [r for r in raw_results if r is not None]
        
        yield_rate = (len(valid_records) / len(chunks)) * 100 if chunks else 0
        logger.info(f"Synthesis Complete. Valid Records Generated: {len(valid_records)}/{len(chunks)} (Yield: {yield_rate:.1f}%)")
        
        if not valid_records:
            logger.error("No valid QA pairs were generated by the LLM. Aborting transaction.")
            return

        # 3. Transactional Persistence
        logger.info("Phase 3: Transactional Persistence to DB...")
        try:
            await self.dao.bulk_insert_golden_records(
                batch_name=batch_name, 
                records=valid_records, 
                created_by="system:dataset_runner"
            )
            
            elapsed_time = time.time() - start_time
            logger.info(f"Successfully published '{batch_name}' benchmark dataset in {elapsed_time:.2f} seconds.")
            
        except Exception as e:
            logger.error(f"Failed to persist benchmark dataset '{batch_name}': {e}")
            raise


async def main():
    logger.info("Initializing Golden Dataset Generation Runner...")

    # Validate API Key
    if not settings.GEMINI_API_KEY or settings.GEMINI_API_KEY.startswith("your_"):
        logger.error("Invalid GEMINI_API_KEY. Please set a valid key in .env")
        sys.exit(1)

    try:
        await init_db_pool()
    except Exception as e:
        logger.error(f"Failed to initialize database pool: {e}")
        sys.exit(1)

    try:
        # 1. Assemble the components (Abstract Factory Pattern)
        llm_factory: ILLMFactory = GeminiLLMFactory()
        
        # Note: We specifically request the TEACHER model (e.g. gemini-3.1-pro-preview) 
        # to ensure high-reasoning capabilities during dataset generation.
        teacher_llm = llm_factory.create_llm(model_name=settings.LLM_TEACHER_MODEL, temperature=0.7)
        
        generator = LangchainDatasetGenerator(llm=teacher_llm)
        dao = PgVectorGoldenRecordDAO()
        
        runner = GoldenDatasetRunner(dao=dao, generator=generator)

        # 2. Configuration for this specific benchmark run
        # In a real environment, these might be parsed via argparse CLI flags.
        BATCH_NAME = "hsbc_2025_eval_v1"
        SAMPLE_SIZE = 50  # Generating 50 professional questions for our benchmark
        TOPIC_RESTRICTION = None # Or ["Financial Performance", "Risk Management"]

        # 3. Execute Pipeline
        await runner.run(
            batch_name=BATCH_NAME, 
            sample_size=SAMPLE_SIZE, 
            topics=TOPIC_RESTRICTION
        )

    except Exception as e:
        logger.exception(f"A fatal error occurred during the dataset generation pipeline: {e}")
        
    finally:
        await close_db_pool()
        logger.info("Runner shutdown complete.")

if __name__ == "__main__":
    asyncio.run(main())
