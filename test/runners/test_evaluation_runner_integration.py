@pytest.mark.asyncio
@pytest.mark.integration
class TestEvaluationRunnerIntegration:
  """
  End-to-End Integration Test for the Phase 5 Evaluation Pipeline.
  Verifies real LLM connectivity, polymorphic judge routing (Case 1 vs Case 2),
  transactional persistence of EAV metrics, and rigorous test data cleanup.
  """

  async def _setup_dummy_inference_data(self) -> str:
    """
    Creates an isolated, dummy inference run with exactly two queries:
    - 1 query with a Ground Truth mapping (Case 1)
    - 1 query with no Ground Truth mapping (Case 2 / Orphaned)
    Returns the generated run_id to be fed into the EvaluationPipeline.
    """
    test_run_id = str(uuid.uuid4())
    q_id_case1 = str(uuid.uuid4())
    q_id_case2 = str(uuid.uuid4())
    gr_id = str(uuid.uuid4())
    
    async with get_db_connection() as conn:
      async with conn.cursor() as cur:
        # 1. Run Metadata
        await cur.execute(
          "INSERT INTO inference_run_history (run_id, start_time, end_time, created_by) VALUES (%s, NOW(), NOW(), 'pytest_eval_runner')", 
          (test_run_id,)
        )
        
        # 2. Query History (Real questions to force the LLM Judge to think)
        await cur.execute("""
          INSERT INTO query_history (query_id, queried_by, question, generated_answer, retrieved_contexts, query_time, retrieval_time, response_time, created_by)
          VALUES 
          (%s, 'pytest', 'What is the capital of France?', 'The capital of France is Paris.', %s, NOW(), NOW(), NOW(), 'pytest_eval_runner'),
          (%s, 'pytest', 'Did HSBC report a profit in 2025?', 'Yes, HSBC reported a net profit of 22.6 billion.', %s, NOW(), NOW(), NOW(), 'pytest_eval_runner')
        """, (
          q_id_case1, json.dumps([]), # Case 1 ignores context anyway
          q_id_case2, json.dumps([{"text": "HSBC Holdings reported a profit of 22.6 billion for the year 2025."}]) # Case 2 needs context for faithfulness
        ))
        
        # 3. Map to Run
        await cur.execute("""
          INSERT INTO inference_run_query_mapping (run_id, query_id, created_by)
          VALUES (%s, %s, 'pytest_eval_runner'), (%s, %s, 'pytest_eval_runner')
        """, (test_run_id, q_id_case1, test_run_id, q_id_case2))
        
        # 4. Golden Record (Case 1)
        await cur.execute(
          "INSERT INTO golden_records (id, batch_name, question, ground_truth, complexity, created_by) VALUES (%s, 'pytest_eval_batch', 'What is the capital of France?', 'Paris is the capital.', 'Factoid', 'pytest_eval_runner')",
          (gr_id,)
        )
        
        # 5. Map Case 1
        await cur.execute(
          "INSERT INTO golden_record_query_mapping (query_id, golden_record_id, created_by) VALUES (%s, %s, 'pytest_eval_runner')",
          (q_id_case1, gr_id)
        )
        
      await conn.commit()
      
    return test_run_id

  async def _cleanup_run_data(self, run_id: str, job_id: str = None):
    """
    Cleans up all queries, metrics, jobs, and golden records generated by this specific test run_id.
    """
    async with get_db_connection() as conn:
      async with conn.cursor() as cur:
        # 1. Identify queries
        await cur.execute("SELECT query_id FROM inference_run_query_mapping WHERE run_id = %s", (run_id,))
        query_ids = [row[0] for row in await cur.fetchall()]

        if query_ids:
          # 2. Identify Golden Records mapped to these queries
          await cur.execute("SELECT golden_record_id FROM golden_record_query_mapping WHERE query_id = ANY(%s)", (query_ids,))
          gr_ids = [row[0] for row in await cur.fetchall()]
          
          if gr_ids:
            await cur.execute("DELETE FROM golden_record_query_mapping WHERE query_id = ANY(%s)", (query_ids,))
            await cur.execute("DELETE FROM golden_records WHERE id = ANY(%s)", (gr_ids,))
          
          # 3. Clean query maps & history
          await cur.execute("DELETE FROM inference_run_query_mapping WHERE run_id = %s", (run_id,))
          await cur.execute("DELETE FROM query_history WHERE query_id = ANY(%s)", (query_ids,))
        
        # 4. Clean Inference Run
        await cur.execute("DELETE FROM inference_run_history WHERE run_id = %s", (run_id,))
        
        # 5. Clean Evaluation Job and Metrics
        if job_id:
          await cur.execute("DELETE FROM evaluation_metrics WHERE job_id = %s", (job_id,))
          await cur.execute("DELETE FROM evaluation_job_history WHERE job_id = %s", (job_id,))
          
      await conn.commit()
      logger.info(f"Test data cleanup complete for run_id: {run_id}, job_id: {job_id}")

  async def test_evaluation_runner_end_to_end_live(self):
    """
    Tests the actual EvaluationPipeline with real Gemini API calls and real database persistence.
    """
    if not settings.GEMINI_API_KEY or settings.GEMINI_API_KEY.startswith("your_"):
      pytest.skip("Skipping live Evaluation Runner test because GEMINI_API_KEY is missing.")
      
    run_id = await self._setup_dummy_inference_data()
    job_id = None
    
    try:
      # 1. Instantiate Concrete Dependencies
      llm_factory = GeminiLLMFactory()
      # Fast model for testing
      judge_llm = llm_factory.create_llm(model_name="gemini-2.5-pro", temperature=0.0)
      
      case1_judge = GoldenBaselineJudge(llm=judge_llm)
      case2_judge = RagTriadJudge(llm=judge_llm)
      dao = PgVectorEvaluationDAO()
      
      # 2. Assemble Pipeline
      pipeline = EvaluationPipeline(
        dao=dao,
        case1_judge=case1_judge,
        case2_judge=case2_judge,
        evaluator_model_name="gemini-2.5-pro-pytest",
        evaluator_prompt_version="v2.0_pytest"
      )
      
      # 3. Execute Pipeline
      summary = await pipeline.run(inference_run_id=run_id, created_by="pytest_runner")
      job_id = summary.get("job_id")
      
      # 4. Assertions
      assert summary["total_queries_evaluated"] == 2
      
      # Case 1 generates 1 metric (correctness), Case 2 generates 3 (RAG Triad) = 4 total metrics
      assert summary["metrics_generated"] == 4
      
      # Verify DB Persistence
      async with get_db_connection() as conn:
        async with conn.cursor() as cur:
          await cur.execute("SELECT metric_name, evaluation_strategy, reasoning, metric_value FROM evaluation_metrics WHERE job_id = %s", (job_id,))
          rows = await cur.fetchall()
          
          assert len(rows) == 4
          metric_names = [row[0] for row in rows]
          strategies = [row[1] for row in rows]
          
          assert "correctness" in metric_names
          assert "faithfulness" in metric_names
          assert "answer_relevance" in metric_names
          assert "context_relevance" in metric_names
          
          assert "CASE1_GROUND_TRUTH" in strategies
          assert "CASE2_RAG_TRIAD" in strategies
          
          # Validate Reasoning length
          for row in rows:
            reasoning = row[2]
            score = row[3]
            assert len(reasoning) > 10, f"Judge failed to provide substantial reasoning for metric {row[0]}"
            assert 0.0 <= score <= 5.0, f"Score out of bounds for metric {row[0]}"
            
    finally:
      # 5. Guaranteed Cleanup
      await self._cleanup_run_data(run_id, job_id)